    public void displayTable(){
       
        
            if (searchBar.getText().equals("")){
           searchBar.setText("Enter your search");
           return;
       }
       
       int minimum = MinimumSlider.getValue();
       int maximum = MaximumSlider.getValue();
       
        DatabaseConnection.ListProcedures yes = new DatabaseConnection.ListProcedures();
        Object[][] display_table = yes.getData(searchBar.getText(), minimum, maximum);
        
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
       model.setRowCount(0);
       
        for (Object[] display_table1 : display_table) {
            model.addRow(display_table1);
        }
         MinimumSlider.setMinimum(0/*Lowest value*/);
         MinimumSlider.setMaximum(100000/*Lowest value*/);
         MaximumSlider.setMaximum(100000/*Hihgest Value*/);
         MaximumSlider.setMinimum(0/*Hihgest Value*/);
         
        
    }
    
    
    
    
